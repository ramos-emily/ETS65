# DESAFIO
# 1.	Sistema de Gerenciamento de Eventos
# Descrição: Implemente um sistema de gerenciamento de eventos, como um software para planejar e organizar eventos corporativos, casamentos, festas, etc. O sistema deve gerenciar o agendamento de eventos, as tarefas associadas a cada evento, os fornecedores e a distribuição de responsabilidades.
# Classes principais: Evento, Tarefa, Fornecedor, Responsável, Convidado, Pagamento..
# Requisitos:
# •	Cada evento tem uma data, hora, local e descrição. Ele pode ter várias tarefas
# associadas (ex: aluguel de espaço, contratação de comida, etc.).
# •	As tarefas têm um responsável, prazo e um status (pendente, concluída, atrasada).
# •	O sistema deve ter um controle de fornecedores (como serviços de catering, decoração, segurança), permitindo associar fornecedores aos eventos.
# •	Pagamentos: O sistema deve permitir o controle de pagamentos relacionados ao evento (parcela, valor total, data de vencimento).
# Desafios adicionais:
# •	Polimorfismo para diferentes tipos de eventos, como casamentos, eventos corporativos ou festas, com regras e exigências diferentes para cada tipo.
# •	Consultas e relatórios: Implemente funções para filtrar eventos por data, status de tarefa ou fornecedor, e gerar relatórios de status de pagamentos e tarefas.
